package exec02;

/**
 * Classe para operacoes matematicas.
 * @author pbido
 * @since 11/02/2020
 * @version 0.1
 */

public class Operacoes {
	
	public static void main(String[] args) {
		
		//Exemplo de adicao
		int adicao = 5+5;
		
		//Exemplo de subtracao
		int subtracao = 7-4;
		
		//Exemplo de multiplicacao
		int multiplicacao = 9*9;
		
		//Exemplo de divisao
		int divisao = 50/2;
		
		//Exemplo de modulo, onde mostra que o resto da operacao 10/2 = 0
		int modulo = 10 % 2;
		
		System.out.println("Adicao: 5+5=" + adicao);
		System.out.println("Subtracao: 7-4=" + subtracao);
		System.out.println("Multiplicacao: 9*9=" + multiplicacao);
		System.out.println("Divisao: 50/2=" + divisao);
		System.out.println("Modulo: 10%2=" + modulo);
		
	}
	
}
